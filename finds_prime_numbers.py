# program to find prime numbers
# taken from the official python tutorial
# 12 january 2017 - phil welsby

for n in range(2, 10):
    for x in range(2, n):
        if n % x == 0:
            print(n, 'equals', x, '*', n/x)
            break
    else:
        # loop fell through without finding a factor
        print(n, 'is a prime number')

        
